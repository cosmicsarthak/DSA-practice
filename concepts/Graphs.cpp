#include <bits/stdc++.h>
using namespace std;
#define endl '\n'

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    vector<vector<int>> v(9);
    cout << v.size() << endl;
    for (auto el : v)
        for (auto e : el)
        {
            cout << e << endl;
        }
    cout << endl;

    return 0;
}